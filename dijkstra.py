import sys
 
class Graph():
 
    def __init__(self, vertices):
        self.V = vertices
        self.graph = [[0 for columna in range(vertices)]
                    for fila in range(vertices)]
 
    def imprimir(self, dist):
        for nodo in range(self.V):
            print(nodo, "\t", dist[nodo])
 
    def distanciaMin(self, dist, sptSet):
 
        min = sys.maxsize
 
        for u in range(self.V):
            if dist[u] < min and sptSet[u] == False:
                min = dist[u]
                index_min = u
 
        return index_min
 
    def dijkstra(self, src):
 
        dist = [sys.maxsize] * self.V
        dist[src] = 0
        sptSet = [False] * self.V
 
        for cout in range(self.V):
 
            x = self.distanciaMin(dist, sptSet)
            
            sptSet[x] = True
 
            for y in range(self.V):
                if self.graph[x][y] > 0 and sptSet[y] == False and \
                dist[y] > dist[x] + self.graph[x][y]:
                        dist[y] = dist[x] + self.graph[x][y]
 
        self.imprimir(dist)
 
